import UIKit

extension UIColor {
    class func backgroundGray() -> UIColor {
        return UIColor(red: 59/255, green: 58/255, blue: 58/255, alpha: 1)
    }

    class func ochreColor() -> UIColor {
        return UIColor(red: 239/255, green: 196/255, blue: 49/255, alpha: 1)
    }

    class func captionGray() -> UIColor {
        return UIColor(red: 164/255, green: 164/255, blue: 164/255, alpha: 1)
    }
}
